package domain;

public class BuildingBean {
	
	private String building_id;
	private String holder_id;		
	private String building_address;	
	private String building_type;		
	private double building_size;			
	private int building_max_hold;
	private double building_cash;
	private String building_state;						
	private String building_note;
	private String building_img;
	public String getBuilding_id() {
		return building_id;
	}
	public void setBuilding_id(String building_id) {
		this.building_id = building_id;
	}
	public String getHolder_id() {
		return holder_id;
	}
	public void setHolder_id(String holder_id) {
		this.holder_id = holder_id;
	}
	public String getBuilding_address() {
		return building_address;
	}
	public void setBuilding_address(String building_address) {
		this.building_address = building_address;
	}
	public String getBuilding_type() {
		return building_type;
	}
	public void setBuilding_type(String building_type) {
		this.building_type = building_type;
	}
	public double getBuilding_size() {
		return building_size;
	}
	public void setBuilding_size(double building_size) {
		this.building_size = building_size;
	}
	public int getBuilding_max_hold() {
		return building_max_hold;
	}
	public void setBuilding_max_hold(int building_max_hold) {
		this.building_max_hold = building_max_hold;
	}
	public double getBuilding_cash() {
		return building_cash;
	}
	public void setBuilding_cash(double building_cash) {
		this.building_cash = building_cash;
	}
	public String getBuilding_state() {
		return building_state;
	}
	public void setBuilding_state(String building_state) {
		this.building_state = building_state;
	}
	public String getBuilding_note() {
		return building_note;
	}
	public void setBuilding_note(String building_note) {
		this.building_note = building_note;
	}
	public String getBuilding_img() {
		return building_img;
	}
	public void setBuilding_img(String building_img) {
		this.building_img = building_img;
	}
}
